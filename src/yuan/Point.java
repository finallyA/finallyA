package yuan;

public class Point {
     double x,y;
     Point (double _x,double _y){
    	  x=_x;
    	  y=_y;
    	 
     }
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
}
